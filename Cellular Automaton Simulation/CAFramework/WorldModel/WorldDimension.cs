namespace CASimulation.CAFramework.WorldModel
{
    public class WorldDimension
    {
        private int length;
        private int height;
        private int depth;

        public int getLength()
        {
            return length;
        }
        public void setLength(int length)
        {
            this.length = length;
        }
        public int getHeight()
        {
            return height;
        }

        public void setHeight(int height)
        {
            this.height = height;
        }
        public int getDepth()
        {
            return depth;
        }
        public void setDepth(int depth)
        {
            this.depth = depth;
        }
    }
}